void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if (htim == IR_OUT_DATA_TIMER)
    ir_out_data_timer_isr();
}

static uint16_t prev_time = 0;

/* timer input capture callback */
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
  /* verify we really have the right timer/channel for IR input */
  if (htim == IR_IN_TIMER && htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1) {
      uint16_t time = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
      /* calculate time difference using unsigned 16-bit integers because timer is 16-bit, and we want unsigned overflow behavior */
      uint16_t delta_t = time - prev_time;

      ir_in_timer_callback(delta_t);
      prev_time = time;
  }
}

